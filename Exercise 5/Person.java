public class Person {

    private String name;
    private int ccnumber;

    public String getName() {
        return this.name;
    }
    
    public void setName(String newName) {

        this.name = newName;
    }

    public int getNum() {
        return this.ccnumber;
    }

    public void setNum(int ccnumber) {
        this.ccnumber = ccnumber;
    }


}
